#### In-class Coding 8 ####

### Sobre o mini-programa:

# O objetivo deste exercício é criar um programa que permita ao usuário:
    # (i) inserir dados;
    # (ii) salvar esses dados em um arquivo e;
    # (iii) ler o arquivo integralmente

# Requisito mínimo: O programa deve oferecer uma interface simples
# que permita ao usuário escolher entre as três opções descritas acima.


### Instruções Gerais para a Equipe:

# 1) O programa deve permitir que o usuário digite dados que serão
#    armazenados em um arquivo de texto.

# 2) O usuário deve ter a opção de ler os dados já armazenados.

# 3) Deve haver uma opção para sair do programa.



